# Program to show the Python Operators 

a = int(input("Enter Number 1:"))
b = int(input("Enter Number 2:"))

addition = a + b
print("The addition result is: \n", addition)

substraction = a - b
print("The substraction result is: \n", substraction)

multiplication = a * b
print("The multiplication result is: \n", multiplication)

division = a / b
print("The division result is: \n", division)

division_floor = a // b 
print("The division in floor result is: \n", division_floor)

modulus = a % b
print("The modulus of these num is: \n", modulus)

power = a ** b
print("The power of these numbers are: \n", power)

print("End of the calculator using operators\n")